import { Metadata } from "next";
import { cookies } from "next/headers";
import React, { Suspense } from "react";
import { Refine } from "@refinedev/core";
import { DevtoolsProvider } from "@providers/devtools";
import { RefineKbar, RefineKbarProvider } from "@refinedev/kbar";
import {
  useNotificationProvider,
  RefineSnackbarProvider,
} from "@refinedev/mui";
import routerProvider from "@refinedev/nextjs-router";

import { dataProvider } from "@providers/data-provider";
import { AppIcon } from "@components/app-icon";
import { ColorModeContextProvider } from "@contexts/color-mode";
import { authProviderClient } from "@providers/auth-provider/auth-provider.client";
import { usersProvider } from "@providers/data-provider/users";

import { MdGroup } from "react-icons/md";
import { sessionsProvider } from "@providers/data-provider/sessions";

import ClientLocalizationProvider from "@lib/client-localization-provider";

export const metadata: Metadata = {
  title: "Refine",
  description: "Generated by create refine app",
  icons: {
    icon: "/favicon.ico",
  },
};

export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const cookieStore = cookies();
  const theme = cookieStore.get("theme");
  const defaultMode = theme?.value === "dark" ? "dark" : "light";

  return (
    <html lang="en">
      <body>
        <Suspense>
          <RefineKbarProvider>
            <ColorModeContextProvider defaultMode={defaultMode}>
              <RefineSnackbarProvider>
                <DevtoolsProvider>
                  <Refine
                    routerProvider={routerProvider}
                    dataProvider={{
                      default: dataProvider,
                      users: usersProvider,
                      sessions: sessionsProvider,
                    }}
                    notificationProvider={useNotificationProvider}
                    authProvider={authProviderClient}
                    resources={[
                      {
                        name: "Users",
                        list: "/users",
                        show: "/users/show/:id",
                        // create: "/users/create",
                        meta: { dataProviderName: "users", icon: <MdGroup /> },
                      },
                      {
                        name: "Sessions",
                        show: "/users/:userId/sessions/:id",
                      },
                      {
                        name: "dashboard",
                        options: { label: "Dashboard" },
                        meta: {
                          canDelete: false,
                        },
                      }
                    ]}
                    options={{
                      syncWithLocation: true,
                      warnWhenUnsavedChanges: true,
                      useNewQueryKeys: true,
                      projectId: "1LxSeQ-Ach9aW-3KZqEZ",
                      title: { text: "Refine Project", icon: <AppIcon /> },
                    }}
                  >
                    <ClientLocalizationProvider>
                      {children}
                    </ClientLocalizationProvider>
                    <RefineKbar />
                  </Refine>
                </DevtoolsProvider>
              </RefineSnackbarProvider>
            </ColorModeContextProvider>
          </RefineKbarProvider>
        </Suspense>
      </body>
    </html>
  );
}
